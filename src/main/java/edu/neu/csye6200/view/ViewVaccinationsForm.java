    public void addRowToJTable( List<Immunization> list)
    
    {
       
        
        Object rowData[] = new Object[list.size()];
       
        
        DefaultTableModel model = (DefaultTableModel) tbl_vaccs.getModel();
       
       
        for(int i = 0; i < list.size(); i++)
        {
            Immunization im = list.get(i);
            rowData[0] = im.getVaccineName();
            rowData[1] = im.getDatesOfVaccination();
            model.addRow(rowData);
            
        }
//        jTable1.setModel(model);
    }

